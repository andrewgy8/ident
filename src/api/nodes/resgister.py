from flask import *

from src.api.nodes.services import NodeService, NodeConflictService
from src.blockchain import blockchain
from . import nodes


@nodes.route('/register', methods=['POST'])
def register_nodes():
    values = request.get_json()

    nodes = values.get('nodes')
    if nodes is None:
        return "Error: Please supply a valid list of nodes", 400

    for node in nodes:
        n = NodeService(blockchain)
        n.register_node(node)

    response = {
        'message': 'New nodes have been added',
        'total_nodes': list(blockchain.nodes),
    }
    return jsonify(response), 201


@nodes.route('/resolve', methods=['GET'])
def consensus():
    c = NodeConflictService(blockchain)
    replaced = c.resolve_conflicts()

    if replaced:
        response = {
            'message': 'Our chain was replaced',
            'new_chain': blockchain.chain
        }
    else:
        response = {
            'message': 'Our chain is authoritative',
            'chain': blockchain.chain
        }

    return jsonify(response), 200
